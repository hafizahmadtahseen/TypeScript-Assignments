// Develop a program that determines the day of the week.
// Ask the user for a number (1-7) and use nested if statements to print the corresponding day's name.
let number = 5;
if (number == 1) {
  console.log(`${number}: It's Monday!`);
} else if (number == 2) {
  console.log(`${number}: It's Tuesday!`);
} else if (number == 3) {
  console.log(`${number}: It's Wednesday!`);
} else if (number == 4) {
  console.log(`${number}: It's Thursday!`);
} else if (number == 5) {
  console.log(`${number}: It's Friday!`);
} else if (number == 6) {
  console.log(`${number}: It's Saturday!`);
} else if (number == 7) {
  console.log(`${number}: It's Sunday!`);
} else {
  console.log("Incorrect Number!");
}
